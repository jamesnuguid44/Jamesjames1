package Lesson4;

public class Adder {

    //private int sum;

    public int Sum(int sum1, int sum2){
        return sum1+sum2;
    }
    public int Sum(int sum1, int sum2, int sum3) {
        return sum1 + sum2 + sum3;
    }
    public int Sum(int sum1, int sum2, int sum3, int sum4) {
        return sum1 + sum2 + sum3 + sum4;
    }
    public int Sum(int sum1, int sum2, int sum3, int sum4, int sum5) {
        return sum1 + sum2 + sum3 + sum4 + sum5;
    }
}
